    public void setCultist(Cultist cultistCard){
        
        cultistModel = cultistCard;
        String name = cultistCard.getName();
        ImageIcon icon;
        
        // Default
        icon = new ImageIcon(getClass().getResource("/GUI/img/sectarios/sect01m.png"));
        
        if (name == "Sectario 2")
             icon = new ImageIcon(getClass().getResource("/GUI/img/sectarios/sect02m.png"));
        else if (name == "Sectario 3")
             icon = new ImageIcon(getClass().getResource("/GUI/img/sectarios/sect03m.png"));
        else if (name == "Sectario 4")
             icon = new ImageIcon(getClass().getResource("/GUI/img/sectarios/sect04m.png"));
        else if (name == "Sectario 5")
             icon = new ImageIcon(getClass().getResource("/GUI/img/sectarios/sect05m.png"));
        else if (name == "Sectario 6")
             icon = new ImageIcon(getClass().getResource("/GUI/img/sectarios/sect06m.png"));
        
        img.setIcon(icon);
        ci = new CultistInfo(cultistModel);
        repaint();
    }
    

