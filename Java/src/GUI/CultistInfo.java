    private void setImage(String name){
        ImageIcon icon;
        
        // Default
        icon = new ImageIcon(getClass().getResource("/GUI/img/sectarios/sect01.png"));
        
        if (name == "Sectario 2")
             icon = new ImageIcon(getClass().getResource("/GUI/img/sectarios/sect02.png"));
        else if (name == "Sectario 3")
             icon = new ImageIcon(getClass().getResource("/GUI/img/sectarios/sect03.png"));
        else if (name == "Sectario 4")
             icon = new ImageIcon(getClass().getResource("/GUI/img/sectarios/sect04.png"));
        else if (name == "Sectario 5")
             icon = new ImageIcon(getClass().getResource("/GUI/img/sectarios/sect05.png"));
        else if (name == "Sectario 6")
             icon = new ImageIcon(getClass().getResource("/GUI/img/sectarios/sect06.png"));
        
        img.setIcon(icon);
        repaint();
    }

